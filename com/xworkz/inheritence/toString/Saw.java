public class Saw {
    private int size;
    private String range;
    private double range;

    public Saw(int size, String range, double range) {
        this.size = size;
        this.range = range;
        this.range = range;
    }

    @Override
    public String toString() {
        return "Saw [size=" + size + ", range=" + range + ", range=" + range + "]";
    }
}

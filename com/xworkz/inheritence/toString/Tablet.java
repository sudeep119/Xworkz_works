public class Tablet {
    private int color;
    private String color;
    private double weight;

    public Tablet(int color, String color, double weight) {
        this.color = color;
        this.color = color;
        this.weight = weight;
    }

    @Override
    public String toString() {
        return "Tablet [color=" + color + ", color=" + color + ", weight=" + weight + "]";
    }
}

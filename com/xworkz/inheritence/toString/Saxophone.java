public class Saxophone {
    private int color;
    private String color;
    private double shape;

    public Saxophone(int color, String color, double shape) {
        this.color = color;
        this.color = color;
        this.shape = shape;
    }

    @Override
    public String toString() {
        return "Saxophone [color=" + color + ", color=" + color + ", shape=" + shape + "]";
    }
}

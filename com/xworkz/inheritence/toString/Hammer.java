public class Hammer {
    private int material;
    private String material;
    private double weight;

    public Hammer(int material, String material, double weight) {
        this.material = material;
        this.material = material;
        this.weight = weight;
    }

    @Override
    public String toString() {
        return "Hammer [material=" + material + ", material=" + material + ", weight=" + weight + "]";
    }
}

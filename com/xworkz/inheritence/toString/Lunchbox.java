public class Lunchbox {
    private int shape;
    private String material;
    private double material;

    public Lunchbox(int shape, String material, double material) {
        this.shape = shape;
        this.material = material;
        this.material = material;
    }

    @Override
    public String toString() {
        return "Lunchbox [shape=" + shape + ", material=" + material + ", material=" + material + "]";
    }
}

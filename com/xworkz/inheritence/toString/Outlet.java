public class Outlet {
    private int volume;
    private String speed;
    private double speed;

    public Outlet(int volume, String speed, double speed) {
        this.volume = volume;
        this.speed = speed;
        this.speed = speed;
    }

    @Override
    public String toString() {
        return "Outlet [volume=" + volume + ", speed=" + speed + ", speed=" + speed + "]";
    }
}

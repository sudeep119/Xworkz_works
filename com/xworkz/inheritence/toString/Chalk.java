public class Chalk {
    private int power;
    private String size;
    private double power;

    public Chalk(int power, String size, double power) {
        this.power = power;
        this.size = size;
        this.power = power;
    }

    @Override
    public String toString() {
        return "Chalk [power=" + power + ", size=" + size + ", power=" + power + "]";
    }
}

public class Sprinkler {
    private int shape;
    private String shape;
    private double intensity;

    public Sprinkler(int shape, String shape, double intensity) {
        this.shape = shape;
        this.shape = shape;
        this.intensity = intensity;
    }

    @Override
    public String toString() {
        return "Sprinkler [shape=" + shape + ", shape=" + shape + ", intensity=" + intensity + "]";
    }
}

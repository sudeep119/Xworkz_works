public class Scalpel {
    private int weight;
    private String weight;
    private double speed;

    public Scalpel(int weight, String weight, double speed) {
        this.weight = weight;
        this.weight = weight;
        this.speed = speed;
    }

    @Override
    public String toString() {
        return "Scalpel [weight=" + weight + ", weight=" + weight + ", speed=" + speed + "]";
    }
}

package com.xworkz.inheritence.internal.wild;

public class Wild {
    public Wild() {
        System.out.println("Running non-arg constructor Wild");
    }
    public void eat() {
        System.out.println("Wild is eating");
    }
    public void sleep() {
        System.out.println("Wild is sleeping");
    }
    public void sound() {
        System.out.println("Wild makes sound");
    }
    public void run() {
        System.out.println("Wild is running");
    }
    public void breathe() {
        System.out.println("Wild is breathing");
    }
}

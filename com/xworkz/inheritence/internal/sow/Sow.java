public class Sow {
    public Sow() {
        System.out.println("Running non-arg constructor Sow");
    }
    public void eatEverything() {
        System.out.println("Sows are omnivores and eat a variety of food");
    }
    public void liveInMud() {
        System.out.println("Sows enjoy rolling in mud to keep cool");
    }
    public void haveStrongSenseOfSmell() {
        System.out.println("Sows have a strong sense of smell");
    }
    public void socialAnimals() {
        System.out.println("Sows are social animals and live in groups");
    }
    public void intelligent() {
        System.out.println("Sows are highly intelligent animals");
    }
}

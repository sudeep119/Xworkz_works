package com.xworkz.inheritence.internal.button;

public class Button {
    public Button() {
        System.out.println("Running non-arg constructor Button");
    }
    public void turnOn() {
        System.out.println("Button turns on the device");
    }
    public void turnOff() {
        System.out.println("Button turns off the device");
    }
    public void saveElectricity() {
        System.out.println("Button helps in saving electricity");
    }
    public void availableInVariousDesigns() {
        System.out.println("Buttons are available in various designs");
    }
    public void usedInHomes() {
        System.out.println("Buttons are commonly used in homes");
    }
}
